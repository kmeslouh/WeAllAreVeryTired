package com.class02;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		double num1=10.99;
		double num2=5.99;
		double sum = num1+num2;
		double sub = num1-num2;
		double mult=num1*num2;
		double div=num1/num2;
		
		System.out.println("The add of 2 numbers " + num1 + " and " + num2+" is equal to "+sum);
		System.out.println("The substract of 2 numbers "+num1+" and "+num2+" is equal to "+sub);
		System.out.println("The multiplication of 2 numbers "+num1+" and "+num2+" is equal to "+mult);
		System.out.println("The division of 2 numbers "+num1+" and "+num2+" is equal to "+div);
		
		float numa=10.99f;
		float numb=5.99f;
		float divi=numa/numb;
		System.out.println("The division of 2 numbers "+numa+" and "+numb+" is equal to "+divi);
		
		

	}

}
